## Палубная служба: от курсанта до капитана

**Стадия 1: Курсант / Кадет (Deck Cadet)**
Это учебная практика во время учёбы в морском вузе. Юридически вы ещё не член экипажа в полном смысле, но учитесь работать на реальном судне. Многие крупные компании набирают кадетов целенаправленно и потом трудоустраивают лучших из них.

**Стадия 2: Третий помощник (3rd Officer)**
Первая офицерская должность после получения диплома. Вахтенный офицер, отвечает за навигационное оборудование, спасательные средства. Минимальный ценз для выхода на эту должность — диплом вахтенного помощника. В реальности — от 12 до 18 месяцев стажа на уровне рядового состава или кадета.

**Стадия 3: Второй помощник (2nd Officer)**
Традиционно отвечает за навигационную документацию, карты, ECDIS. Чтобы перейти со 2-го на 2nd: нужен определённый стаж вахтенного помощника (обычно не менее 12 месяцев), в ряде случаев сдача экзаменов. Реальный срок перехода от 3rd к 2nd — 2-3 года.

**Стадия 4: Старший помощник (Chief Officer / Chief Mate)**
Это уже серьёзная позиция. ChOff отвечает за весь груз, остойчивость, состояние палубной команды и является первым кандидатом на замену капитана. Минимальный стаж вахтенного помощника — обычно 36 месяцев суммарно. Для получения диплома Chief Mate нужно сдать экзамен. Реально — от 5 до 8 лет с начала карьеры.

**Стадия 5: Капитан (Master)**
Вершина палубной карьеры. После получения диплома Chief Mate нужен стаж в этой должности — от 12 месяцев и более, в зависимости от требований флага. Потом экзамен на диплом Master. Реальный срок от старта до капитанского мостика — 10–15 лет при целенаправленной работе.

Бывает быстрее? Да. Некоторые доходят до Captain за 8-9 лет — как правило, те, кто работает без длинных перерывов на берегу и целенаправленно собирает ценз. Бывает медленнее? Тоже да.

## Машинная служба: от моториста до старшего механика

Механическая карьера строится по похожей схеме, только "единица измерения" — киловатты главного двигателя, а не дедвейт судна.

**Моторист / Кадет-механик** — начальный уровень. Понимание судовых систем, работа в машинном отделении под руководством.

**Четвёртый / Третий механик (4th / 3rd Engineer)** — первые офицерские вахты в машинном отделении. Обслуживание вспомогательных механизмов, насосов, сепараторов.

**Второй механик (2nd Engineer)** — технически очень ответственная позиция. Часто именно Second Engineer является главным "практическим" механиком на борту, занимается непосредственно ГД и основными системами.

**Старший механик (Chief Engineer)** — аналог капитана в машинном отделении. Полная ответственность за механическую часть. Диплом Chief Engineer — серьёзный экзамен. Реальный путь от моториста — от 10 до 15 лет.

Дефицит хороших механиков на рынке сейчас заметнее, чем дефицит палубных офицеров. Это значит, что хорошему механику с правильными документами найти выгодный контракт проще.

