## Ошибки, которые разрушают отношения

**Исчезать на связи без предупреждения.** Понятно, что бывают напряжённые вахты, порты, сложные суточные переходы. Но если вы пропадаете на несколько дней без слова — дома начинают волноваться и обижаться. Простое "буду недоступен следующие двое суток, всё нормально" делает огромную разницу.

**Приходить домой и сразу брать на себя "командование".** Полгода отсутствовали — дома выстроился свой порядок, своя система. Врываться с "а почему это так, а вот это неправильно" — путь в никуда. Домой нужно входить мягко, давать время всем привыкнуть обратно.

**Не говорить о деньгах заранее.** Сколько уйдёт на семью, сколько на сбережения, как это организовать — всё это нужно обсуждать до отхода, а не по возвращении. Финансовые недопонимания — частая причина конфликтов.

**Уходить в телефон весь отпуск.** После длинного контракта бывает соблазн просто отлежаться. Это понятно, и немного времени на восстановление необходимо. Но три недели в режиме "я устал, не трогайте" — это не отпуск, это другое море, только дома.

**Не вовлекать партнёра в морскую жизнь.** Когда жена или муж совсем не понимают, чем вы занимаетесь — это разрыв в общем пространстве. Рассказывайте. Где были. Что видели. Какие порты запомнились. Это сближает.

