## Контейнеровозы (Container Ships)

Перевозят контейнеры — то, что мы покупаем в магазинах, в значительной мере приехало к нам на контейнеровозе. Флот очень разнообразный: от небольших фидерных судов до гигантских Ultra Large Container Vessels с вместимостью 20 000+ TEU.

**Плюсы:** Как правило, современные суда с хорошим техническим оснащением. Регулярные линии — значит, предсказуемое расписание. На крупных линейных компаниях (Maersk, MSC, CMA CGM) хорошие условия труда и неплохая зарплата. Интересные порты захода.

**Минусы:** На крупных контейнеровозах портовые стоянки очень короткие — 12-24 часа, иногда меньше. "Увидеть мир" в классическом смысле не получится — времени сойти на берег почти нет. Рабочий темп напряжённый.

**Кому подходит:** Тем, кто любит динамику и чёткий ритм. Тем, кому важна стабильность компании. Отличный выбор для карьерного роста в крупных международных компаниях.

## Нефтяные танкеры (Oil Tankers)

Перевозят нефть и нефтепродукты. Делятся по размеру: от небольших product tankers до огромных VLCC (Very Large Crude Carrier).

**Плюсы:** Зарплаты выше, чем на балкерах. Специализированная работа с танкерными операциями — ценный опыт на рынке труда. Серьёзные судовладельцы уделяют много внимания безопасности и техническому состоянию судов.

**Минусы:** Повышенные требования к документам — нужны танкерные сертификаты. Ответственность за грузовые операции высокая — ошибки дорогостоящие. Специфические условия работы с нефтепродуктами.

**Кому подходит:** Тем, кто хочет зарабатывать больше и готов углубиться в специализацию. Тем, кто уже имеет базовый стаж и хочет перейти на более высокооплачиваемый сегмент.

## Газовозы (Gas Carriers: LPG и LNG)

Перевозят сжиженный газ — LPG (пропан, бутан) или LNG (природный газ). Самый высокооплачиваемый сегмент торгового флота.

**Плюсы:** Зарплаты — самые высокие в торговом флоте. Профессионалы на газовозах в большом дефиците — их ценят и держат. Технически очень интересная работа, постоянное развитие.

**Минусы:** Высочайшие требования к документам и квалификации. Попасть на газовоз без опыта на танкерах практически нереально — это следующий шаг после нефтяного танкера. Очень серьёзная ответственность.

**Кому подходит:** Опытным офицерам с танкерным стажем, которые готовы к дополнительному обучению и хотят максимального дохода. Это вершина с точки зрения зарплаты в торговом флоте.

## Химические танкеры (Chemical Tankers)

Перевозят химические вещества — часто несколько разных грузов одновременно в разных танках.

**Плюсы:** Технически очень интересная работа. Хорошая зарплата. Высокий спрос на специалистов.

**Минусы:** Повышенные требования к безопасности. Специальные сертификаты обязательны. Специфические условия работы.

## Офшорный флот (Offshore Vessels)

Это суда, которые обслуживают нефтяные и газовые платформы: PSV (Platform Supply Vessels), AHTS (Anchor Handling), MPSV (Multi-Purpose Support Vessels), буровые полупогружные платформы.

**Плюсы:** Зарплаты очень хорошие, особенно при наличии DP-сертификата. Интересная работа, часто в технически сложных условиях. Ротация нередко 4 недели работы / 4 недели дома — лучше, чем стандартные 4-6 месяцев.

**Минусы:** DP-сертификация — длительный и дорогостоящий процесс. Работа в суровых условиях (Северное море, Арктика). Рынок более волатилен — зависит от цен на нефть.

**Кому подходит:** Тем, кто ценит более короткие ротации и готов инвестировать в DP-сертификацию. Хороший вариант для баланса работы и личной жизни.

## Пассажирские суда и круизные лайнеры

Отдельный мир. Огромные суда, тысячи пассажиров, строгий сервисный стандарт.

**Плюсы:** Интересный опыт. Посещение красивых портов. Некоторые компании (Carnival, Royal Caribbean, MSC Cruises) платят вполне хорошо.

**Минусы:** Работа с пассажирами добавляет напряжения. Стандарты внешнего вида и поведения строже, чем на обычном торговом флоте. На круизниках часто работают большие смешанные экипажи — своя специфика.

## Паромы и каботаж

Короткие маршруты, регулярные линии, суда возвращаются в порт каждый день или каждые несколько дней.

**Плюсы:** Домой чаще. Режим понятный и предсказуемый. Подходит для семейных людей или тех, кто хочет быть ближе к дому.

**Минусы:** Зарплаты ниже, чем на международном флоте. Работа более монотонная.

