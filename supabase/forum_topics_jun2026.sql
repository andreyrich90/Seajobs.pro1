## Типичные вопросы и как на них отвечать

**"Расскажите о своём последнем контракте."**
Не пересказывайте всё подряд. Скажите: тип судна, ваша должность, что было особенного или интересного, что сделали хорошо. Конкретно, по делу, без воды.

**"Почему вы ушли с предыдущего места?"**
Честно, но дипломатично. "Компания сократила флот", "искал другой тип судов", "семейные обстоятельства" — нормальные ответы. "Капитан был идиот" — нет, даже если так и было.

**"Как вы действуете при пожаре в машинном отделении?"**
Это проверка на базовые знания. Не паникуйте, отвечайте по процедуре. Если где-то не уверены — скажите честно, что в конкретной ситуации первым делом сверились бы с SMS (системой управления безопасностью).

**"Какие у вас слабые стороны?"**
Классический вопрос. Не говорите "у меня нет слабых сторон" — это звучит либо самонадеянно, либо неискренне. Называйте слабую сторону, которую вы осознаёте и над которой работаете.

**"Почему хотите работать именно у нас?"**
Вот здесь пригодится то, что вы узнали о компании. Конкретные причины всегда работают лучше общих фраз.

**Технические вопросы по специальности.**
Здесь всё просто: знаете — отвечаете. Не знаете — говорите честно и объясняете, как бы нашли ответ. Врать на технических вопросах — плохая идея, это проверяется на судне в первый же день.

