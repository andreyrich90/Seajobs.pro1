## Рабочее время: где граница

MLC устанавливает два варианта учёта рабочего времени, и судовладелец выбирает один из них.

**Вариант А — максимальные часы работы:**
- Не более 14 часов в любые 24 часа
- Не более 72 часов в любые 7 дней

**Вариант Б — минимальные часы отдыха:**
- Не менее 10 часов отдыха в любые 24 часа
- Не менее 77 часов отдыха в любые 7 дней

Часы отдыха можно разделить максимум на два периода, один из которых должен быть не менее 6 часов непрерывно.

Это минимальные стандарты. Если на вашем судне систематически нарушается режим отдыха, а вахтенные журналы "рисуются" под проверку — это нарушение MLC, и с ним можно и нужно работать.

## Зарплата: когда и как должны платить

Заработная плата должна выплачиваться не реже одного раза в месяц. Задержка зарплаты — прямое нарушение конвенции.

Моряк имеет право переводить часть зарплаты семье. Судовладелец обязан обеспечить такую возможность — это не привилегия, это право.

При увольнении все причитающиеся суммы должны быть выплачены не позднее, чем через 30 дней с момента окончания контракта.

Если зарплату задерживают — это повод обратиться к ITF-инспектору в ближайшем порту. Об этом ниже.

## Репатриация: домой за чей счёт

Это один из самых важных пунктов. Моряк имеет право на репатриацию — возвращение домой за счёт судовладельца — в следующих случаях:

- Истечение срока контракта
- Болезнь или травма (когда требуется лечение на берегу)
- Кораблекрушение
- Несостоятельность судовладельца (он обанкротился и не может платить)
- Нарушение судовладельцем условий контракта
- Работа в зоне вооружённого конфликта, если моряк не давал согласия

Затраты на репатриацию включают: транспорт до места репатриации (обычно страна регистрации или место найма), питание и жильё в пути, медицинское сопровождение при необходимости.

Если судовладелец отказывается организовать репатриацию или не может этого сделать (банкротство), подключается флаговое государство.

## Медицина на борту и на берегу

Судовладелец обязан обеспечить медицинское обслуживание на борту — включая необходимые медикаменты и медицинское оборудование согласно требованиям. Бесплатно для моряка.

При необходимости сход с судна для лечения — расходы на медицинскую помощь несёт судовладелец. Это работает, пока моряк находится на контракте.

Важный момент: при травме или болезни, связанной с работой, право на медицинское обеспечение сохраняется минимум 16 недель после схода с судна. Это прямо прописано в конвенции.

## Жильё и питание на судне

MLC устанавливает минимальные стандарты жилья на борту: размер каюты, вентиляция, освещение, санузлы. Если условия жилья откровенно не соответствуют стандартам — это тоже нарушение.

Питание за счёт судовладельца — не опция, а обязанность. Оно должно быть достаточным по количеству и отвечать базовым требованиям качества.

## Что делать, если права нарушают

**Шаг 1: Внутренние процедуры.** На каждом судне должна быть процедура подачи жалоб. Моряк имеет право подать жалобу капитану и получить ответ. Если жалоба на самого капитана — обращайтесь к судовладельцу напрямую.

**Шаг 2: ITF-инспектор.** Международная федерация транспортных рабочих (ITF) имеет инспекторов в большинстве крупных портов мира. Их контакты есть на сайте itfseafarers.org. Инспектор ITF может вмешаться в ситуацию прямо в порту — задержать судно до урегулирования вопросов.

**Шаг 3: Флаговое государство.** Обратитесь к администрации флага судна — у каждого флага есть морская администрация, которая обязана реагировать на жалобы от моряков своих судов.

**Шаг 4: Портовый государственный контроль (PSC).** Если судно зашло в порт — инспекторы PSC проверяют в том числе соблюдение MLC. Вы имеете право пожаловаться им.

