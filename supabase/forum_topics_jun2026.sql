## Признаки того, что агентству можно доверять

**Есть лицензия и она проверяема.** В России крюинговые агентства регистрируются и работают в правовом поле. Нормальное агентство без проблем называет своё юридическое название, ИНН, адрес офиса. Если на сайте нет никаких юридических данных — плохой знак.

**С вас не берут денег.** Это не просто правило хорошего тона — это международная норма. Конвенция MLC 2006, Правило 1.4 прямо запрещает агентствам взимать плату с моряков за трудоустройство. Любые "регистрационные взносы", "платное размещение в базе", "гарантированный контракт за деньги" — всё это нарушение. Точка.

**Называют судовладельца до подписания контракта.** Порядочное агентство не скрывает, с какой компанией вы будете работать. Вы имеете право знать название судовладельца, флаг судна и основные условия контракта до того, как что-либо подписали.

**Менеджеры отвечают на конкретные вопросы.** Попробуйте спросить: "Какие суда в управлении у вашего судовладельца?", "Какой коллективный договор применяется?", "Как оформляется репатриация?" Нормальный менеджер ответит конкретно. Уклончивые ответы и "это мы обсудим после регистрации" — тревожный сигнал.

