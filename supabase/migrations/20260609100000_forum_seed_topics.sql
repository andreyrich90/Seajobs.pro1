## Береговые профессии для опытных моряков

**Суперинтендант (Superintendent)**

Это, пожалуй, самый естественный переход для опытного морского офицера. Суперинтендант — это береговой представитель судовладельца, который контролирует техническое состояние судов флота, организует ремонты, общается с экипажем и следит за соблюдением стандартов.

Морской суперинтендант — для механиков. Навигационный или операционный — для палубных офицеров.

Зарплата в европейских и азиатских судоходных компаниях — от $4 000 до $10 000 в месяц в зависимости от компании и региона. Плюс командировки на суда, что многих вполне устраивает — остаётся связь с морем без долгих контрактов.

**Морской инспектор / Классификационный инспектор**

Работа в классификационных обществах — Lloyd's Register, Bureau Veritas, DNV, RINA. Инспекторы проверяют суда на соответствие стандартам, проводят освидетельствования, выдают сертификаты.

Требования: опыт командного состава, техническое образование, языки. Зарплата хорошая, условия работы цивилизованные, командировки регулярные.

**PSC-инспектор**

Работа в портовом государственном контроле — инспектируете суда в порту. Это государственная служба, условия зависят от страны. В некоторых странах это престижная и хорошо оплачиваемая позиция.

**Флит-менеджер (Fleet Manager)**

Управление флотом со стороны берега: операционные вопросы, ротация экипажей, взаимодействие с агентами и портами. Требует организационных навыков и глубокого понимания морских операций.

**Крюинговый менеджер**

Подбор и управление экипажами в крюинговых агентствах или департаментах судовладельца. Понимание рынка труда моряков, требований к документам, особенностей должностей — всё это у вас уже есть.

**Морской преподаватель / Тренер**

Учебные центры для моряков нуждаются в людях с реальным практическим опытом. Преподавать STCW, вести тренажёрные занятия, обучать кризисному управлению — для этого нужна именно практика, а не только теория.

**Лоцман**

Лоцман проводит суда в портах и по опасным маршрутам. Требует серьёзного стажа, местного знания и лицензии. Зарплаты очень достойные, режим работы удобный.

