**Профессионализм — лучший аргумент.** Звучит банально, но это правда. Женщина, которая отлично знает своё дело, вызывает уважение — вне зависимости от пола.

**Выбор правильного судовладельца.** Есть компании с чёткой политикой в отношении гендерного равенства, программами менторства для женщин-офицеров и нулевой терпимостью к дискриминации.

**Профессиональные сети.** WISTA International (Women's International Shipping & Trading Association) — организация, объединяющая женщин в морской отрасли. Там есть менторские программы, нетворкинг, поддержка. Для тех, кто начинает карьеру — полезный ресурс.

