## С чем реально придётся столкнуться

**Бытовые условия.** На старых судах отдельных женских кают и санузлов может не быть — это решается при оформлении контракта (уточняйте заранее). На современных судах и в нормальных компаниях этот вопрос решён.

**Отношение в коллективе.** Бывает разным — как везде. Большинство профессиональных моряков воспринимают коллегу-женщину нормально, если она делает свою работу хорошо. Но предрассудки встречаются, и к этому стоит быть готовой психологически.

**Физические требования.** Некоторые аспекты морской работы физически тяжёлые — особенно в рядовом составе. Это объективный фактор, не повод отказываться, но стоит реалистично оценивать свою физическую форму.

**Вопросы беременности и материнства.** При наступлении беременности моряк имеет право на репатриацию. Многие женщины успешно совмещают карьеру на флоте с материнством, выбирая подходящие контракты и сегменты.

