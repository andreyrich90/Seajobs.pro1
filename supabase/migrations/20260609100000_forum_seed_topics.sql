**Цитадель (Citadel)**

Специально укреплённое помещение на судне, куда экипаж может эвакуироваться при нападении и держаться до прибытия военно-морских сил. Дверь бронированная, есть связь с внешним миром, запасы воды и еды.

**Противопиратские меры BMP (Best Management Practices)**

